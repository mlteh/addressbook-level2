package seedu.addressbook.data.person;

public class PostalCode {
	
	
	private String postalCode;
	
	public PostalCode(String ps){
		postalCode = ps;
	}
	
	//Setters
	
	public void setPostalCode(String ps2){
		postalCode = ps2;		
	}
	
	//Getters
	public String getPostalCode(){
		return postalCode;
	}
	
	

}